"""Object-oriented programming (OOP) is a programming paradigm that is based on the concept of "objects",
which can contain data (attributes or properties) and code (methods or functions). OOP focuses on modeling 
real-world objects and their relationships, and is based on four fundamental concepts:

Encapsulation: the idea of hiding the internal details of an object from the outside world, and exposing a 
well-defined interface for interacting with the object.

Inheritance: the idea of creating new classes by deriving them from existing classes, 
inheriting their attributes and methods and adding new ones.

Polymorphism: the idea of using a single interface to represent different types of objects, and 
allowing objects of different classes to be used interchangeably.

Abstraction: the idea of representing complex real-world systems in terms of simpler and 
more abstract models, by identifying the relevant properties and ignoring the irrelevant ones.

Java supports all these four OOP concepts, including inheritance, polymorphism, abstraction, and encapsulation.

Java also supports multiple inheritance, but only through interfaces. Java does not support multiple 
inheritance through classes, which means that a class cannot inherit from more than one class. However, a
class can implement multiple interfaces, which allows it to inherit abstract methods and constants from those
interfaces.

In Java, multiple inheritance through interfaces is supported by allowing a class to implement multiple 
interfaces. An interface defines a set of methods that a class must implement, but does not provide any implementation
itself. By implementing multiple interfaces, a class can inherit methods and constants from all of those
interfaces, and use them in its own implementation."""